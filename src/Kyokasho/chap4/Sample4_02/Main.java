package Kyokasho.chap4.Sample4_02;

public class Main {
    public static void main(String[] args) {
        int a = 5;
        while(a > 0) {
            System.out.print(a);
            a--;
        }
        System.out.println("\n--------");
        int b = 5;
        while(b >= 0) 
            b--;
        System.out.print(b);
    }
}
